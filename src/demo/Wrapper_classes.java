package demo;

public class Wrapper_classes {

	public static void main(String[] args) {
		
		// Java Wrapper Class
		
		// To create a wrapper object, use the wrapper class method instead of the... 
		//...primitive data type
		
		
		Integer myint = 5; //int
		Double mydouble = 5.99; //double
		Character mychar = 'A'; //Char
		
		System.out.println(myint);
		System.out.println(mychar);
		System.out.println(mydouble);
		
		//intValue(),ByteValue(),shortValue etc....
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
